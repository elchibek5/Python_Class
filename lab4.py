# 1. Count with a While loop
# initializing number and going until we hit our while loop condition
number = 1
while number <= 5:
    print(number)
    number += 1

# 2. Count with a List with For Loop
# initializing our list and iterating each variable
fruits = ["Apple", "Banana", "Cherry", "Grapes"]
# Python


for fruit in fruits:
    print(fruit)

