def calculate_sum(num1, num2):
    result = num1 + num2
    print(result)

# Calling the function with 8 and 12
calculate_sum(8, 12)

def square_number(num):
    return num * num

# Calling the function with 6 and printing the result
result = square_number(6)
print(result)

def minutes_to_seconds(minutes):
    return minutes * 60

# Calling the function with 10 and printing the result
seconds = minutes_to_seconds(10)
print(seconds)